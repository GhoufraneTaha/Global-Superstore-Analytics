
DROP TABLE IF EXISTS retail_sales;

CREATE TABLE retail_sales (
    Category TEXT,
    City TEXT,
    Country TEXT,
    "Customer.ID" TEXT,
    "Customer.Name" TEXT,
    Discount TEXT,
    Market TEXT,
    "Order.Date" TIMESTAMP,
    "Order.ID" TEXT,
    "Order.Priority" TEXT,
    "Product.ID" TEXT,
    "Product.Name" TEXT,
    Profit TEXT,
    Quantity INTEGER,
    Region TEXT,
    "Row.ID" INTEGER,
    Sales TEXT,
    Segment TEXT,
    "Ship.Date" TIMESTAMP,
    "Ship.Mode" TEXT,
    "Shipping.Cost" TEXT,
    State TEXT,
    "Sub-Category" TEXT,
    Year INTEGER,
    Market2 TEXT,
    weeknum INTEGER
);





































